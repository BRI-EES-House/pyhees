# 付録 C 仮想居住人数
# ============================================================================

def get_n_p(A_A):
    """付録 C 仮想居住人数

    Args:
      A_A(float): 床面積

    Returns:
      float: 仮想居住人数

    """
    if A_A < 30:
        return 1.0
    elif A_A < 120:
        return A_A / 30
    else:
        return 4.0
