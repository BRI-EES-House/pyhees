# 付録 C 仮想居住人数
# ============================================================================

def get_n_p(A_A):
    """付録 C 仮想居住人数
    
    :param A_A: 床面積
    :type A_A: float
    :return: 仮想居住人数
    :rtype: float
    """
    if A_A < 30:
        return 1.0
    elif A_A < 120:
        return A_A / 30
    else:
        return 4.0
